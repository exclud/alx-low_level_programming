#include <stdio.h>
/**
 * main - Entry point
 * Description: Prints the name of the program
 * @argc: The number of command-line arguments
 * @argv: An array of command-line argument strings
 * Return: 0 (Success)
 */
#include <stdio.h>

int main(int argc, char *argv[])
{
    (void)argc;
    printf("%s\n", argv[0]);
    return(0);
}

